def get_custom_error_page(code: int) -> str:
    file_map = {
        404: os.path.join(HIDD, "errors", "404.html"),
        403: os.path.join(HIDD, "errors", "403.html"),
        400: os.path.join(HIDD, "errors", "400.html"),
        503: os.path.join(HIDD, "errors", "503.html")
    }
    target_file = file_map.get(code)
    if target_file and os.path.exists(target_file):
        try:
            with open(target_file, "r", encoding="utf-8") as f:
                return f.read().format(PORT=PORT)
        except Exception as e:
            print(f"[SERVER] Error reading {code} page: {e}")
    return f"<h1>Error {code}</h1>"

