## Process apis
@mcp.tool("runProcess")
def processSection_runProcess(pathtoexe: str, args: str = "") -> dict[str, object]:
    """
    Returns: Dict containing the result of running a process.
    Args:
        pathtoexe: Path to the executable to run.
        args: Arguments to pass to the executable (default empty).
    Description: Runs a process and captures its output and return code.
    """
    try:
        result = run([pathtoexe] + args.split(), stdout=PIPE, stderr=PIPE, text=True)
        return {
            "message": f"Process '{pathtoexe}' executed successfully.",
            "stdout": result.stdout,
            "stderr": result.stderr, 
        }
    except Exception as e:
        return {
            "message": f"Error executing process '{pathtoexe}': {e}",
            "returncode": -1,
            "stdout": "",
            "stderr": str(e),
        }

