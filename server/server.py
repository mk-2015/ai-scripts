@mcp.tool("generateFakeData")
def fakeDataSection_generateFakeData(count: int = 8) -> dict[str, object]:
    """
    Returns: Dict containing generated fake data for testing purposes.
    Args:
        count: number of records to generate (default 8)
    Description: Generates a small list of realistic-looking test records.
    """
    import random
    from datetime import datetime

    first_names = ["Alice", "Marcus", "Sofia", "Liam", "Zoe", "Noah", "Maya", "Ethan"]
    last_names = ["Nguyen", "Patel", "Martinez", "O'Connor", "Kim", "Brown", "Singh", "Williams"]
    cities = ["Seattle", "Austin", "San Diego", "Dublin", "Seoul", "London", "Mumbai", "Toronto"]

    records: list[dict] = []
    for i in range(count):
        fn = random.choice(first_names)
        ln = random.choice(last_names)
        name = f"{fn} {ln}"
        email_local = f"{fn}.{ln}".lower().replace("'", "")
        rec = {
            "id": randbelow(1000000),
            "uuid": str(uuid.uuid4()),
            "name": name,
            "email": f"{email_local}@example.com",
            "created_at": datetime.now(datetime.timezone.utc).replace(microsecond=0).isoformat() + "Z",
            "is_active": bool(randbelow(2)),
            "profile": {
                "age": 18 + randbelow(50),
                "city": random.choice(cities),
                "score": round(randbelow(10000) / 100.0, 1),
            },
        }
        records.append(rec)

    return {
        "message": "Fake data generated by fakeDataSection_generateFakeData",
        "count": len(records),
        "data": records,
    }

